public class math{

    /**
     * Sums two numbers
     *
     * @param numberOne The first number to be sum.
     * @param numberTwo The second number to be sum.
     */
    public static int sum(int numberOne, int numberTwo) {
        return numberOne + numberTwo;
    }

}